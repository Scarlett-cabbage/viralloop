// ========== 主生成函数 ==========
export function generatePlanForPersona(personaId, persona) {
    // 查找专属计划
    let planData = PERSONA_PLAN_MAP[personaId];

    // 如果没有专属计划，使用通用生成器
    if (!planData) {
        const generic = generateGenericPlan(personaId, persona);
        planData = {
            planTitle: `${persona.name || '内容创作者'}·7天增长计划`,
            followerData: generic.followerData,
            days: generic.days,
        };
    }

    // 构建完整的plan数组
    const plan = planData.days.map((day, i) => ({
        day: i + 1,
        date: getDateString(i),
        title: day.title,
        status: i < 6 ? 'completed' : 'active',
        tasks: day.tasks,
        contentTitle: day.contentTitle,
        contentType: day.contentType,
        tag: day.tag,
        followers: day.followers,
        likes: day.likes,
        comments: day.comments,
        shares: day.shares,
        views: day.views,
        aiExplain: day.aiExplain,
    }));

    // 构建编辑器数据
    const editorData = {};
    planData.days.forEach((day, i) => {
        editorData[i + 1] = {
            title: day.contentTitle.replace(/[「」]/g, ''),
            hook: day.hook,
            body: day.body,
            cta: day.cta,
            tags: day.tags,
            structure: day.structure,
        };
    });

    return {
        plan,
        editorData,
        followerData: planData.followerData,
    };
}

// 辅助函数：生成日期字符串
function getDateString(dayOffset) {
    const base = new Date('2026-04-21');
    base.setDate(base.getDate() + dayOffset);
    return base.toISOString().split('T')[0];
}
